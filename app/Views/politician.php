    <h2><span class="fa fa-info"></span> <a href="http://www.elections.ca/content.aspx?section=ele&dir=2015ge&document=index&lang=e">Get Ready To Vote</a></h2>
    <p>I strongly recommend looking up Election’s Canada ‘<a href="http://www.elections.ca/content.aspx?section=ele&dir=2015ge&document=index&lang=e">Get Ready to Vote 2014</a>‘ guide to make sure you are registered and ready to vote in your home. If you are a student away from home or have accessibility concerns you can find that information in this guide.</p>
