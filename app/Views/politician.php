        <h2><span class="fa fa-pencil-square-o"></span> <a href="https://ereg.elections.ca/CWelcome.aspx">Voter Registration</a></h2>
        <p>Are you registered to vote? Check if you are <a href="https://ereg.elections.ca/CWelcome.aspx">registered at your current home</a> to vote in the upcoming election using the Elections Canada website.</p>
        
        <h2><span class="fa fa-user"></span> <a href="http://www.elections.ca/content.aspx?section=ele&dir=2015ge/IDinfo&document=text&lang=e">Voter ID requirements</a></h2>
        <p>If you would like to vote you must have proper ID. If you have a driver’s licence that is all you need. If you do not have a driver’s license, please consult the <a href="http://www.elections.ca/content.aspx?section=ele&dir=2015ge/IDinfo&document=text&lang=e">voter ID requirements</a>.</p>
